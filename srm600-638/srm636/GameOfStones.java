        int count = stones.length;
        for (int s : stones) {
            if ((s % 2 == 1) != odd) {
                return -1;
            }
            sum += s;
        }
        if (sum % count != 0) return -1;
        int avg = sum / count;
        if ((avg % 2 == 1) != odd) return -1;

        // valid
